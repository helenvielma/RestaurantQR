	<h3>¿Qué es un codigo QR?</h3>
	Un código QR (quick response code, «código de respuesta rápida») 
	es un módulo útil para almacenar información en una matriz de 
	puntos o un código de barras bidimensional creado por la 
	compañía japonesa Denso Wave, subsidiaria de Toyota, en 1994. 
	Se caracteriza por los tres cuadrados que se encuentran en las 
	esquinas y que permiten detectar la posición del código al 
	lector. La sigla «QR» se deriva de la frase inglesa Quick Response 
	(Respuesta Rápida en español), pues los creadores 
	(Joaco Retes y Euge Damm1 ) aspiran a que el código permita 
	que su contenido se lea a alta velocidad. Los códigos QR son 
	muy comunes en Japón y de hecho son el código bidimensional más popular en ese país.
	<br />
	Aunque inicialmente se usó para registrar repuestos en el área de la 
	fabricación de vehículos, hoy los códigos QR se usan para administración 
	de inventarios en una gran variedad de industrias. La inclusión de software 
	que lee códigos QR en teléfonos móviles, ha permitido nuevos usos orientados
	 al consumidor, que se manifiestan en comodidades como el dejar de tener que 
	 introducir datos de forma manual en los teléfonos.
